# Tipos int e float  
# int: números inteiros, sem parte decimal
# float: números reais, com parte decimal
# int sem sinal é considerado positivo, mas pode ser negativo.
# Exemplos de int
print(10)  # int positivo
print(-5)  # int negativo 
print(0)   # int zero    

# Exemplos de float
print(3.14, 10.11)   # float positivo
print(-2.5)   # float negativo
print(0.0)    # float zero  

# A função type() é usada para verificar o tipo de uma variável ou valor
print(type(1.1))
print(type(10))